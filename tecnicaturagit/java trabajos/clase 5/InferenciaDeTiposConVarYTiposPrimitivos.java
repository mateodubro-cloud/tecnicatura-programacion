
package inferencia.de.tipos.con.var.y.tipos.primitivos;


public class InferenciaDeTiposConVarYTiposPrimitivos {

    
    public static void main(String[] args) {
        var numEntero = 20;
        System.out.println("numEntero = " + numEntero);
        var numFloat = 10.0F;
        System.out.println("numFloat = " + numFloat);
        var numDouble = 10.0;
        System.out.println("numDouble = " + numDouble);
    }
    
}
